int      fb_font_loaded(void) { return 1; }
uint32_t fb_font_w(void)      { return FONT_W * g_scale; }
uint32_t fb_font_h(void)      { return FONT_H * g_scale; }

void fb_font_set_scale(uint32_t s)
{
    if (s < 1) s = 1;
    if (s > 8) s = 8;
    g_scale = s;
}

uint32_t fb_font_get_scale(void) { return g_scale; }

void fb_draw_glyph(uint32_t x, uint32_t y, uint32_t fg_rgb, uint32_t bg_rgb,
                   uint32_t cp)
{
    if (cp < FONT_FIRST || cp > FONT_LAST) cp = '?';
    const uint8_t *glyph = g_glyphs[cp - FONT_FIRST];

    uint32_t fg_px = fb_rgb_to_pixel(fg_rgb);
    uint32_t bg_px = fb_rgb_to_pixel(bg_rgb);
    const fb_desc_t *fb = fb_get();
    uint32_t s = g_scale;
    uint8_t bpp = fb->bpp >> 3;

    for (uint32_t row = 0; row < FONT_H; row++) {
        uint8_t bits = glyph[row];
        for (uint32_t sy = 0; sy < s; sy++) {
            uint8_t *dst = (uint8_t *)fb->base
                         + (y + row * s + sy) * fb->pitch
                         + x * bpp;
            for (uint32_t col = 0; col < FONT_W; col++) {
                uint32_t px = (bits >> (7u - col)) & 1u ? fg_px : bg_px;
                for (uint32_t sx = 0; sx < s; sx++) {
                    if (fb->bpp == 32) {
                        *(uint32_t *)dst = px;
                        dst += 4;
                    } else {
                        dst[0] = (uint8_t)px;
                        dst[1] = (uint8_t)(px >> 8);
                        dst[2] = (uint8_t)(px >> 16);
                        dst += 3;
                    }
                }
            }
        }
    }
}
