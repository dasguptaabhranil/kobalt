#include <stdint.h>
#include <kfmt.h>

void kobalt_log_critical(const char *tag, const void *data, uint64_t len)
{
    (void)data; (void)len;
    klog_fail(tag ? tag : "tykid", "critical audit event");
}

uint64_t kobalt_tsc_read(void)
{
    uint32_t lo, hi;
    __asm__ volatile("rdtsc" : "=a"(lo), "=d"(hi));
    return ((uint64_t)hi << 32) | lo;
}
